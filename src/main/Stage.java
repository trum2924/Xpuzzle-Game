    @Override
    public void actionPerformed(ActionEvent ae) {
        ButtonPlz b = (ButtonPlz) ae.getSource();
        for (Integer swapAble : b.getSwapAble()) {
            ButtonPlz bDes = (ButtonPlz) pane.getComponent(swapAble);
            if (bDes.getText().equals("")) {
                swapButton(b, bDes);
            }
        }
        System.out.println(checkWin());
         if (checkWin()) {
                //show message winner
                int x = JOptionPane.showConfirmDialog(this, "You win, New game?", "Congratulation", JOptionPane.YES_OPTION);
                if (x == JOptionPane.YES_OPTION) {
                    btnNewGameActionPerformed(ae);
                }
            }
    }

