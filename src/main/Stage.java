    public void initXxX(int x) {
        count = 0;
        lbcount.setText("0");
        listXxX = new ArrayList<>();
        pane.setLayout(new GridLayout(x, x));
        pane.setSize(300,300);

        ArrayList<Integer> randomlist = new ArrayList<>();
        for (int i = 0; i < x * x; i++) {
            randomlist.add(i);
        }
        Collections.shuffle(randomlist);
        for (int i = 0; i < x * x; i++) {
            String text = randomlist.get(i).toString();
            if (text.equals("0")) {
                text = "";
            }
            ButtonPlz bPlz = new ButtonPlz(i, x, text);
            bPlz.addActionListener(this);
            listXxX.add(bPlz);
        }
        squaretify(x);
        for (int i = 0; i < x * x; i++) {
            pane.add(listXxX.get(i));
        }
        this.add(pane);
    }

    public void squaretify(int x) {
        //cat tren
        for (int i = 0; i < x; i++) {
            listXxX.get(i).removeASwapAble(-x);
        }
        //cat duoi
        for (int i = x * x - x; i < x * x; i++) {
            listXxX.get(i).removeASwapAble(x);
        }
        //cat trai
        for (int i = 0; i <= x * x - x; i += x) {
            listXxX.get(i).removeASwapAble(-1);
        }
        //cat phai
        for (int i = x - 1; i < x * x; i += x) {
            listXxX.get(i).removeASwapAble(1);
        }
    }

    public void swapButton(ButtonPlz src, ButtonPlz des) {
        des.setText(src.getText());
        src.setText("");
        lbcount.setText(Integer.toString(count+=1));
    }

    public boolean checkWin() {
        for (int i = 0; i < pane.getComponentCount() - 1; i++) {
            int x = 0;
            String s = ((ButtonPlz) pane.getComponent(i)).getText();
            if (!s.equals("")) {
                x = Integer.parseInt(s);
            }
            if (x != (i + 1)) {
                return false;
            }
        }
        return true;
    }

